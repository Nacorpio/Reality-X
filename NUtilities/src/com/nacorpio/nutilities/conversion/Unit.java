	public static final class Force {
		
		/**
		 * Returns the work of the specified parameters.
		 * @param par1 the force in newtons.
		 * @param par2 the distance in meters.
		 * @return the work in joules.
		 */
		public static final float getWork(float force, float distance) {
			return force * distance;
		}
		
		/**
		 * Returns the distance of the specified parameters.
		 * @param work the work in joules.
		 * @param force the force in newtons.
		 * @return the distance in meters.
		 */
		public static final float getDistance(float work, float force) {
			return work / force;
		}
		
		/**
		 * Returns the force generated with the specified parameters.
		 * @param work the work in joules.
		 * @param distance the distance in meters.
		 * @return the force in newtons.
		 */
		public static final float getForce(float work, float distance) {
			return work / distance;
		}
		
	}

